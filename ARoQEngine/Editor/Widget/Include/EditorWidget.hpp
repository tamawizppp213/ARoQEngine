namespace editor
{
	/****************************************************************************
	*				  			   EditorWidget
	****************************************************************************/
	/* @class     EditorWidget
	*  @brief     Editorに関わるオブジェクト全ての基底クラスです.
	*             UIの扱い方 (ドラッグドロップした場合の処理などを記述します.)
	*****************************************************************************/
	class EditorWidget : public gu::NonCopyable
	{
	public:
		/****************************************************************************
		**                Public Function
		*****************************************************************************/

		/****************************************************************************
		**                Public Property
		*****************************************************************************/
		/*----------------------------------------------------------------------
		*  @brief : widgetが有効かどうか (入力受付ができるかどうか)
		*----------------------------------------------------------------------*/
		bool IsActive() const { return _isActive; }

		/*----------------------------------------------------------------------
		*  @brief : widgetが有効かを設定します.
		*----------------------------------------------------------------------*/
		void SetActive(const bool isActive) { _isActive = isActive; }

		/****************************************************************************
		**                Constructor and Destructor
		*****************************************************************************/
		EditorWidget();

		virtual ~EditorWidget();

	protected:
		/****************************************************************************
		**                Protected Function
		*****************************************************************************/

		/****************************************************************************
		**                Protected Property
		*****************************************************************************/
		bool _isActive = false;
	};

}
