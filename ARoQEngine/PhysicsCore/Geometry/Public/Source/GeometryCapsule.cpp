bool GeometryCapsule::IsValid() const
{
	if (_geometryType != GeometryType::Capsule) { return false; }

	if ((!isfinite(Radius)) || (!isfinite(HalfHeight)))
	{
		return false;
	}

	if (Radius <= 0.0f || HalfHeight <= 0.0f)
	{
		return false;
	}

	return true;
}