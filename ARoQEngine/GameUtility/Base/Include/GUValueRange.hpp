namespace gu
{
	/****************************************************************************
	*				  			ValueRange
	*************************************************************************//**
	*  @class     ValueRange
	*  @brief     From Start to End
	*****************************************************************************/
	template<typename T>
	struct ValueRange
	{
	public:
		/****************************************************************************
		**                Public Function
		*****************************************************************************/
		T GetValueSize() { return End - Start; }
		/****************************************************************************
		**                Public Property
		*****************************************************************************/
		T Start = T();
		T End = T();

		/****************************************************************************
		**                Constructor and Destructor
		*****************************************************************************/
		ValueRange() = default;
		ValueRange(const T& start, const T& end) : Start(start), End(end) {};

	};
}
#endif